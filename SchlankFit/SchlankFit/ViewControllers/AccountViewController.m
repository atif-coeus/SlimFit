- (UITableViewCell *)tableView:(UITableView *)tableView cellForRowAtIndexPath:(NSIndexPath *)indexPath
{
    static NSString *CellIdentifier = @"LeftImageCell";
    UITableViewCell *cell = [LeftImageCell tableView:tableView cellWithLeftImageWithIdentifier:CellIdentifier];
    
    if (indexPath.row == 0) {
        [(LeftImageCell*)cell setTitle:@"Mein Profil" withIcon:nil];
    } else if (indexPath.row == 1){
        [(LeftImageCell*)cell setTitle:@"Mein Kochbuch" withIcon:nil];
    }
    
    return cell;
}

